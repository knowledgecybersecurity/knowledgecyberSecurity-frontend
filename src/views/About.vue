      </div>

      <br/>
      <div>
        <h4>Application and Purpose</h4>
        <p>Using the NICE framework as a foundational reference will improve the communication necessary to identify, recruit and develop cybersecurity talent. 
The NICE framework will enable employers to use consistent and focused language in professional development programs, in their use of industry certifications and academic credentials, and in their selection of relevant training opportunities for their workforce. 
The NICE framework facilitates the use of a more consistent, comparable and repeatable system approach to selecting and specifying cybersecurity roles for positions within organizations. 
It also provides a common lexicon that academic institutions can use to develop cybersecurity curricula that better prepares students for current and anticipated cybersecurity workforce needs. 
Applying the NICE Framework provides the ability to describe all cybersecurity work. 
        </p>

      </div>

      <br/>
      <div>
        <h4>Objective</h4>
        <p>The applicability objective of the NICE Framework is that any cybersecurity job or position can be described by identifying relevant material from one or more components of the NICE Framework. For each job or position, the context of the mission or business processes and priorities will determine what material is selected from the NICE Framework.
Organizations or sectors may use the NICE Framework to develop additional publications or tools to meet their needs to define or provide guidance on different aspects of workforce development, planning, training, and education.</p>

