        <h4>What is it?</h4>
        <p>Workforce Framework for Cybersecurity (NICE Framework), is a framework that provides a set of building blocks to describe the tasks, knowledge, and skills needed to perform cybersecurity work performed by individuals and teams. Through these core components, the NICE Framework enables organizations to develop their workforce to perform cybersecurity work, and helps learners explore cybersecurity work and engage in appropriate learning activities to develop their knowledge and skills.</p>
